    private void bt_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_cadastrarActionPerformed
        String id = tf_id.getText();
        String nome = tf_nome.getText();
        String email = tf_email.getText();
        String senha = tf_senha.getText();

        modelo.addRow(new String[]{id, nome, email, senha});
        tabela.setModel(modelo);

        SalvarNoTXT cadastros = new SalvarNoTXT();
        cadastros.setId(tf_id.getText());
        cadastros.setNome(tf_nome.getText());
        cadastros.setEmail(tf_email.getText());
        cadastros.setSenha(tf_senha.getText());
        JOptionPane.showMessageDialog(null, cadastros.salvar());
        tf_id.setText("");
        tf_nome.setText("");
        tf_email.setText("");
        tf_senha.setText("");
    }//GEN-LAST:event_bt_cadastrarActionPerformed

    private void bt_pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_pesquisarActionPerformed
        String pesquisa = tf_pesquisar.getText();

        for (int i = 0; i < modelo.getRowCount(); i++) {
            if (pesquisa.equals(tabela.getValueAt(i, a))) {
                tabela.setRowSelectionAllowed(true);
                tabela.setRowSelectionInterval(i, a);
            }
        }
        
        tf_pesquisar.setText("");
    }//GEN-LAST:event_bt_pesquisarActionPerformed

    private void bt_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_excluirActionPerformed
        String pesquisa = tf_pesquisar.getText();
        for (int i = 0; i < modelo.getRowCount(); i++) {
            if (pesquisa.equals(tabela.getValueAt(i, a))) {
                modelo.removeRow(i);
            }
        }
    }//GEN-LAST:event_bt_excluirActionPerformed

    private void bt_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_editarActionPerformed
        int numLinha = tabela.getSelectedRow();

        tf_id.setText((String) tabela.getValueAt(numLinha, a));
        tf_nome.setText((String) tabela.getValueAt(numLinha, b));
        tf_email.setText((String) tabela.getValueAt(numLinha, c));
        tf_senha.setText((String) tabela.getValueAt(numLinha, d));

        bt_cadastrar.setEnabled(false);
        bt_salvar.setEnabled(true);
    }//GEN-LAST:event_bt_editarActionPerformed

