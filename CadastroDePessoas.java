    private void bt_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_salvarActionPerformed
        int numLinha = tabela.getSelectedRow();
        String id = tf_id.getText();
        String nome = tf_nome.getText();
        String email = tf_email.getText();
        String senha = tf_senha.getText();

        tabela.setValueAt(id, numLinha, a);
        tabela.setValueAt(nome, numLinha, b);
        tabela.setValueAt(email, numLinha, c);
        tabela.setValueAt(senha, numLinha, d);
        
        tf_id.setText("");
        tf_nome.setText("");
        tf_email.setText("");
        tf_senha.setText("");
        
        bt_cadastrar.setEnabled(true);
        bt_salvar.setEnabled(false);
    }//GEN-LAST:event_bt_salvarActionPerformed

